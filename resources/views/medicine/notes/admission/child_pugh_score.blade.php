<div class="col-md-12">
	<h2><span class="label label-success">Child-Pugh Scoring</span></h2>
</div>
<div class="col-md-12">
	<p>The score employs five clinical measures of liver disease. Each measure is scored 1-3, with 3 indicating most severe derangement.</p>
</div>
